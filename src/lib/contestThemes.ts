import type { ContestTheme } from '@/types/theme'

/** Derive rgba accent variants from a hex accent color */
function hexToRgb(hex: string): string {
  const h = hex.replace('#', '')
  const r = parseInt(h.slice(0, 2), 16)
  const g = parseInt(h.slice(2, 4), 16)
  const b = parseInt(h.slice(4, 6), 16)
  return `${r},${g},${b}`
}

/**
 * Build a full ContestTheme from a partial config (used for custom DB themes).
 * Derives missing fields from accent color + navVariant.
 */
export function buildCustomTheme(config: Record<string, any>): ContestTheme {
  const accent: string = config.accent ?? '#6b9478'
  const isDark: boolean = (config.navVariant ?? 'dark') === 'dark'
  const rgb = hexToRgb(accent)

  return {
    slug: config.slug ?? 'custom',
    name: config.name ?? 'Custom',
    tagline: config.tagline ?? '',

    bg: config.bg ?? (isDark
      ? 'linear-gradient(160deg, #0d1212 0%, #0a0d0d 100%)'
      : 'linear-gradient(160deg, #eeeae3 0%, #e8e3db 100%)'),
    bgSecondary: config.bgSecondary ?? (isDark ? '#080d09' : '#e4ded6'),
    surface: config.surface ?? (isDark ? 'rgba(255,255,255,0.06)' : 'rgba(255,255,255,0.68)'),
    surfaceBorder: config.surfaceBorder ?? (isDark ? `rgba(${rgb},0.14)` : 'rgba(0,0,0,0.07)'),
    surfaceHover: config.surfaceHover ?? (isDark ? 'rgba(255,255,255,0.09)' : 'rgba(255,255,255,0.85)'),

    textPrimary:   config.textPrimary   ?? (isDark ? 'rgba(220,240,220,0.92)' : '#18160f'),
    textSecondary: config.textSecondary ?? (isDark ? 'rgba(200,220,200,0.75)' : '#3a3830'),
    textMuted:     config.textMuted     ?? (isDark ? `rgba(${rgb},0.50)`       : '#8a8274'),
    textAccent:    config.textAccent    ?? (isDark ? `rgba(${rgb},0.90)`       : accent),

    accent,
    accentBg:     config.accentBg     ?? `rgba(${rgb},0.12)`,
    accentBorder: config.accentBorder ?? `rgba(${rgb},0.26)`,

    displayFont: config.displayFont ?? 'Instrument Serif',
    monoFont:    'JetBrains Mono',

    particleColor: config.particleColor ?? `rgba(${rgb},0.12)`,
    glowColor:     config.glowColor     ?? `rgba(${rgb},0.28)`,
    gridOverlay:   config.gridOverlay   ?? false,
    grainOverlay:  config.grainOverlay  ?? isDark,

    doodlePaths: {
      hero:            config.doodlePaths?.hero            ?? '',
      background:      config.doodlePaths?.background      ?? '',
      empty:           config.doodlePaths?.empty           ?? '',
      problemDivider:  config.doodlePaths?.problemDivider  ?? '',
    },
    doodleBlend:   config.doodleBlend   ?? (isDark ? 'screen' : 'multiply'),
    doodleOpacity: config.doodleOpacity ?? 0.10,
    navVariant:    config.navVariant    ?? 'dark',
  } as ContestTheme
}

